public class QuestionJ5 {
    public static void main(String[] args){
        int x = 15;
        x += 5; // x = x + 5
        System.out.println("After += : " + x);
        x /= 4; // x = x / 4
        System.out.println("After /= : " + x);
        x++;
        System.out.println("After ++ : " + x);
        x %= 3;
        System.out.println("After %= : " + x);
    }
}
